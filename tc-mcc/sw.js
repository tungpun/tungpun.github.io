const CACHE_NAME = 'mcc-lookup-v1';
const ASSETS_TO_CACHE = [
  '/tc-mcc/',
  '/tc-mcc/index.html',
  '/tc-mcc/assets/bootstrap.min.css',
  '/tc-mcc/assets/bootstrap-icons.css',
  '/tc-mcc/assets/favicon.svg',
  '/tc-mcc/assets/fonts/bootstrap-icons.woff2',
  '/tc-mcc/assets/fonts/bootstrap-icons.woff'
];

// Install event - cache assets
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        return cache.addAll(ASSETS_TO_CACHE);
      })
      .then(() => {
        return self.skipWaiting();
      })
  );
});

// Activate event - clean old caches
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => {
      return self.clients.claim();
    })
  );
});

// Fetch event - network first, fallback to cache
self.addEventListener('fetch', (event) => {
  // Skip non-GET requests
  if (event.request.method !== 'GET') return;
  
  // Skip API requests (let them go through network)
  if (event.request.url.includes('tra-cuu-mcc.vercel.app')) {
    return;
  }

  event.respondWith(
    fetch(event.request)
      .then((response) => {
        // Clone the response before caching
        const responseClone = response.clone();
        caches.open(CACHE_NAME).then((cache) => {
          cache.put(event.request, responseClone);
        });
        return response;
      })
      .catch(() => {
        // If network fails, try cache
        return caches.match(event.request);
      })
  );
});

